/**
 * Template manifest — single source of truth for what `template.yml`
 * may contain and which fields are required (#585).
 *
 * Until this module existed, the same regex pattern was duplicated in
 * four files (templateLabel.ts, templateTier.ts, templateSchemaVersion.ts,
 * stackInstall/dependencies.ts) and twice more inline in registry.ts,
 * each parsing one annotation. Build-time tests then re-asserted some
 * (but not all) of those rules with hand-rolled regexes of their own.
 * Adding a field meant editing five files plus two docs.
 *
 * This module unifies the parsing into one function and one field-spec
 * table that drives both runtime validation and the autogenerated
 * field-reference section in docs/TEMPLATE_AUTHORING.md.
 *
 * Adding a field:
 *   1. extend TemplateManifest below
 *   2. add a `TEMPLATE_FIELDS` entry with required/default/description
 *   3. extend parseTemplateManifest with the field's parsing rule
 *   4. run `npm run gen-template-docs` to refresh the docs table
 *
 * No client/server split: the parser is pure (no fs / Node deps) and is
 * safe to import from React components.
 */

import { type TemplateApiVersions, type TemplateApiName, SUPPORTED_API_VERSIONS } from './apiVersions';
import { parseReadinessYaml } from '@/lib/install/readiness/parse';

/** Recognized template tiers. `feature` is the implicit default. */
export type TemplateTier = 'infrastructure' | 'feature';
const KNOWN_TIERS: ReadonlySet<TemplateTier> = new Set(['infrastructure', 'feature']);

/** Parsed template metadata. Stable shape — both runtime + tests rely on it. */
export interface TemplateManifest {
  /** `metadata.annotations['servicebay.label']` — friendly UI label. Required. */
  label: string;
  /**
   * `metadata.annotations['servicebay.tier']`. `feature` is the implicit
   * default — anything not in KNOWN_TIERS is an error.
   */
  tier: TemplateTier;
  /**
   * `metadata.annotations['servicebay.schema-version']`. Positive integer.
   * Defaults to 1 when the annotation is missing.
   */
  schemaVersion: number;
  /**
   * `metadata.annotations['servicebay.dependencies']` — comma-separated
   * template names this template needs installed first. Empty array when
   * missing.
   */
  dependencies: string[];
  /**
   * `metadata.annotations['servicebay.config-mount']` — container mountPath
   * the wizard should write companion `*.mustache` files into. Required
   * only when the template ships `*.mustache` files (caller signals via
   * ParseContext.hasMustacheConfigs).
   */
  configMount?: string;
  /**
   * `metadata.annotations['servicebay.ports']` — comma-separated `port/proto`
   * list (e.g. `"8080/tcp,8443/tcp"`). Recommended; no validation beyond
   * "is a string" today.
   */
  ports?: string;
  /**
   * `metadata.annotations['servicebay.requires-api.<name>']` (#588).
   * Each declared key is the version of a ServiceBay-internal `/api/system/<name>/*`
   * surface the template's `post-deploy.py` calls. Core refuses to invoke
   * `post-deploy.py` if the template requests a version this core can't satisfy.
   * Empty / missing = template makes no such calls.
   */
  requiresApi?: TemplateApiVersions;
  /**
   * `metadata.annotations['servicebay.readiness']` (#613) — raw YAML body of
   * the readiness probe list. Stored unparsed here because the parser runs
   * twice over the template lifecycle:
   *   1. At lint/test time, on the pre-Mustache template, just to assert
   *      the structure is well-formed (with `{{VAR}}` placeholders left
   *      as opaque strings).
   *   2. At install time, on the Mustache-rendered body, to get the
   *      concrete probe values to execute.
   * `parseTemplateManifest` runs step 1 and surfaces structural errors;
   * the install runner runs step 2 and executes the probes.
   */
  readinessRaw?: string;
}

/** Context the caller can pass to enable conditional rules. */
export interface ParseContext {
  /** True if the template directory contains `*.mustache` companion config
   *  files. When set, `servicebay.config-mount` becomes required — without
   *  the annotation the resolver falls back to a `/config`-suffix heuristic
   *  that silently picks the wrong mount in multi-volume pods. */
  hasMustacheConfigs?: boolean;
}

export type ParseResult =
  | { ok: true; manifest: TemplateManifest; warnings: string[] }
  | { ok: false; errors: string[]; warnings: string[] };

/**
 * Field-spec table. Single source of truth for both parser behavior and
 * the autogenerated docs table. Order is the order entries appear in the
 * generated docs (most-important first).
 */
export interface TemplateFieldSpec {
  /** Annotation key inside `metadata.annotations`. */
  annotation: string;
  /** Manifest field name. */
  field: keyof TemplateManifest;
  /** `true` always required; `'if-mustache-configs'` required iff the
   *  template ships `*.mustache` files; `false` optional. */
  required: true | false | 'if-mustache-configs';
  /** Default applied when annotation is missing (and field is not required). */
  default?: string | number | string[];
  /** Enum of allowed values, if any. */
  validValues?: readonly string[];
  /** One-line description for the docs table. */
  description: string;
}

