/**
 * Pull a multi-line YAML block scalar (`key: |` or `key: >`) out of raw
 * template YAML. Indentation-anchored: captures every subsequent line
 * indented strictly more than the key line, stops at the first line that
 * dedents back to (or past) the key. Returns the block body with the
 * leading per-line indentation stripped — i.e. the raw YAML you'd feed
 * into a follow-up `yaml.load`.
 */
function readBlockScalarAnnotation(yamlText: string, annotation: string): string | undefined {
  const escaped = annotation.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const headerRe = new RegExp(`^(\\s+)${escaped}:\\s*([|>])[+-]?\\s*$`, 'm');
  const header = headerRe.exec(yamlText);
  if (!header) return undefined;
  const keyIndent = header[1].length;
  const afterHeader = yamlText.slice(header.index + header[0].length);
  const lines = afterHeader.split('\n');
  // First line after the header is the start of the block; consume lines
  // indented strictly deeper than the key. A blank line is part of the
  // block if the next non-blank line is still indented.
  const blockLines: string[] = [];
  let blockIndent: number | null = null;
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (line.trim() === '') { blockLines.push(''); continue; }
    const indent = line.length - line.trimStart().length;
    if (indent <= keyIndent) break;
    if (blockIndent === null) blockIndent = indent;
    blockLines.push(line.slice(Math.min(indent, blockIndent)));
  }
  // Trim trailing blank lines that aren't part of any indented content.
  while (blockLines.length > 0 && blockLines[blockLines.length - 1].trim() === '') {
    blockLines.pop();
  }
  if (blockLines.length === 0) return undefined;
  return blockLines.join('\n') + '\n';
}

/**
 * Pull a single annotation value out of raw YAML text. Regex-based on
 * purpose: this function runs against pre-mustache-substitution YAML
 * (the wizard collects variables before render), and our templates
 * contain `{{PLACEHOLDER}}` strings that pass js-yaml's parser
 * incidentally — we don't want to depend on that. Annotation values
 * themselves are always literal strings in our templates.
 *
 * Matches `key: "..."`, `key: '...'`, or `key: bare value` (until end
 * of line). Anchored to an annotation-style indented line so it doesn't
 * latch onto references inside multi-line scalars or comments.
 */
function readAnnotation(yamlText: string, annotation: string): string | undefined {
  // Escape regex metacharacters in the annotation key (the `.` in
  // `servicebay.label` would otherwise match anything).
  const escaped = annotation.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const re = new RegExp(
    `^\\s+${escaped}:\\s*(?:"([^"]*)"|'([^']*)'|([^\\n#]+?))\\s*$`,
    'm',
  );
  const m = re.exec(yamlText);
  if (!m) return undefined;
  const raw = (m[1] ?? m[2] ?? m[3] ?? '').trim();
  return raw === '' ? undefined : raw;
}

/**
 * Parse a template.yml's manifest annotations. Pure function — caller
 * passes the YAML text plus optional context flags. Returns either a
 * complete manifest or a list of human-readable errors.
 */
export function parseTemplateManifest(
  yamlText: string,
  ctx: ParseContext = {},
): ParseResult {
  const errors: string[] = [];
  const warnings: string[] = [];

  const label = readAnnotation(yamlText, 'servicebay.label');
  if (label === undefined) {
    errors.push(
      'Missing required annotation `servicebay.label`. Add it under `metadata.annotations` ' +
      'with a friendly display name (e.g. `servicebay.label: "Vaultwarden (Passwords)"`).',
    );
  }

  const ports = readAnnotation(yamlText, 'servicebay.ports');

  const configMount = readAnnotation(yamlText, 'servicebay.config-mount');
  if (ctx.hasMustacheConfigs && !configMount) {
    errors.push(
      'Template ships *.mustache config files but has no `servicebay.config-mount` annotation. ' +
      'Without it the resolver falls back to a `/config`-suffix heuristic that silently picks the ' +
      'wrong mount in multi-volume pods. Add `servicebay.config-mount: "<container mountPath>"` ' +
      'pointing at the volume the configs should land in.',
    );
  }

  let schemaVersion = 1;
  const schemaRaw = readAnnotation(yamlText, 'servicebay.schema-version');
  if (schemaRaw !== undefined) {
    const n = Number.parseInt(schemaRaw, 10);
    if (!Number.isFinite(n) || n < 1 || !/^\d+$/.test(schemaRaw)) {
      // Strict: typos and stray decimals/letters surface as errors rather
      // than silently defaulting to 1 (which was the old behavior and
      // hid bugs).
      errors.push(
        `Annotation \`servicebay.schema-version\` must be a positive integer; got "${schemaRaw}".`,
      );
    } else {
      schemaVersion = n;
    }
  }

  let tier: TemplateTier = 'feature';
  const tierRaw = readAnnotation(yamlText, 'servicebay.tier');
  if (tierRaw !== undefined) {
    if (KNOWN_TIERS.has(tierRaw as TemplateTier)) {
      tier = tierRaw as TemplateTier;
    } else {
      errors.push(
        `Annotation \`servicebay.tier\` must be one of ${[...KNOWN_TIERS].map(v => `"${v}"`).join(', ')}; ` +
        `got "${tierRaw}".`,
      );
    }
  }

  const dependencies: string[] = [];
  const depsRaw = readAnnotation(yamlText, 'servicebay.dependencies');
  if (depsRaw !== undefined) {
    for (const name of depsRaw.split(',').map(s => s.trim()).filter(Boolean)) {
      dependencies.push(name);
    }
  }

  // requiresApi (#588): per-API annotations of shape
  // `servicebay.requires-api.<name>: "<integer>"`. Returns undefined
  // when no such annotation exists so downstream code can short-circuit.
  let requiresApi: TemplateApiVersions | undefined;
  for (const api of Object.keys(SUPPORTED_API_VERSIONS) as TemplateApiName[]) {
    const raw = readAnnotation(yamlText, `servicebay.requires-api.${api}`);
    if (raw === undefined) continue;
    const n = Number.parseInt(raw, 10);
    if (!Number.isFinite(n) || n < 1 || !/^\d+$/.test(raw)) {
      errors.push(
        `Annotation \`servicebay.requires-api.${api}\` must be a positive integer; got "${raw}".`,
      );
      continue;
    }
    requiresApi = { ...(requiresApi ?? {}), [api]: n };
  }

  // readinessRaw (#613): pre-Mustache YAML body of the readiness probe list.
  // Validated structurally here — values may contain `{{VAR}}` placeholders
  // that the install runner substitutes before re-parsing at deploy time.
  const readinessRaw = readBlockScalarAnnotation(yamlText, 'servicebay.readiness');
  if (readinessRaw !== undefined) {
    // `permissive: true` — the raw template still has `{{VAR}}` placeholders
    // in fields like `port:` and `timeout:`. The install runner re-parses
    // strictly after Mustache substitution.
    const r = parseReadinessYaml(readinessRaw, { permissive: true });
    if (!r.ok) {
      for (const err of r.errors) {
        errors.push(`Annotation \`servicebay.readiness\`: ${err}`);
      }
    }
  }

  if (errors.length > 0) {
    return { ok: false, errors, warnings };
  }

  return {
    ok: true,
    manifest: {
      label: label!,
      tier,
      schemaVersion,
      dependencies,
      configMount,
      ports,
      requiresApi,
      readinessRaw,
    },
    warnings,
  };
}

/**
 * Compact helper for callers that only care whether a manifest exists.
 * Returns `null` when parsing fails. Use the full `parseTemplateManifest`
 * when you need the error list (registry sync, consistency tests).
 */
export function tryParseTemplateManifest(
  yamlText: string,
  ctx: ParseContext = {},
): TemplateManifest | null {
  const r = parseTemplateManifest(yamlText, ctx);
  return r.ok ? r.manifest : null;
}

/**
 * Permissive extraction — returns a `Partial<TemplateManifest>` with
 * whatever annotations are present, no validation. Used by the legacy
 * per-field wrappers (templateLabel.ts, templateTier.ts,
 * templateSchemaVersion.ts, stackInstall/dependencies.ts) so they can
 * keep operating on fragmentary YAML (e.g. tests passing only one
 * annotation) without tripping the strict required-fields check.
 *
 * New code should NOT use this — call `parseTemplateManifest` and
 * surface the error list.
 */
export function readManifestAnnotations(yamlText: string): Partial<TemplateManifest> {
  const out: Partial<TemplateManifest> = {};

  const label = readAnnotation(yamlText, 'servicebay.label');
  if (label !== undefined) out.label = label;

  const ports = readAnnotation(yamlText, 'servicebay.ports');
  if (ports !== undefined) out.ports = ports;

  const configMount = readAnnotation(yamlText, 'servicebay.config-mount');
  if (configMount !== undefined) out.configMount = configMount;

  const schemaRaw = readAnnotation(yamlText, 'servicebay.schema-version');
  if (schemaRaw !== undefined) {
    const n = Number.parseInt(schemaRaw, 10);
    if (Number.isFinite(n) && n >= 1 && /^\d+$/.test(schemaRaw)) {
      out.schemaVersion = n;
    }
  }

  const tierRaw = readAnnotation(yamlText, 'servicebay.tier');
  if (tierRaw !== undefined && KNOWN_TIERS.has(tierRaw as TemplateTier)) {
    out.tier = tierRaw as TemplateTier;
  }

  const depsRaw = readAnnotation(yamlText, 'servicebay.dependencies');
  if (depsRaw !== undefined) {
    out.dependencies = depsRaw.split(',').map(s => s.trim()).filter(Boolean);
  }

  let requiresApi: TemplateApiVersions | undefined;
  for (const api of Object.keys(SUPPORTED_API_VERSIONS) as TemplateApiName[]) {
    const raw = readAnnotation(yamlText, `servicebay.requires-api.${api}`);
    if (raw === undefined) continue;
    const n = Number.parseInt(raw, 10);
    if (Number.isFinite(n) && n >= 1 && /^\d+$/.test(raw)) {
      requiresApi = { ...(requiresApi ?? {}), [api]: n };
    }
  }
  if (requiresApi) out.requiresApi = requiresApi;

  const readinessRaw = readBlockScalarAnnotation(yamlText, 'servicebay.readiness');
  if (readinessRaw !== undefined) out.readinessRaw = readinessRaw;

  return out;
}
